#addition operation
def myAdd(num1,num2):
    return num1+num2

#sub operation
def mySub(num1,num2):
    return num1-num2

#mul operation
def myMul(num1,num2):
    return num1*num2

#div operation
def myDiv(num1,num2):
    return num1/num2

result1 = myAdd(20,30)
result2 = mySub(60,20)
result3 = myMul(2,6)
result4 = myDiv(10,2)

print(result1)
print(result2)
print(result3)
print(result4)